#!/usr/bin/env node
/**
 * Writes .env.local from `supabase status -o env` (API_URL, ANON_KEY).
 * Falls back to lib/local-supabase.ts defaults if CLI is not running.
 */
import { execSync } from "node:child_process"
import { writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"

const root = join(dirname(fileURLToPath(import.meta.url)), "..")
const envPath = join(root, ".env.local")

const fallback = {
  NEXT_PUBLIC_SUPABASE_URL: "http://127.0.0.1:54621",
  NEXT_PUBLIC_SUPABASE_ANON_KEY:
    "eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZS1kZW1vIiwicm9sZSI6ImFub24iLCJleHAiOjE5ODM4MTI5OTZ9.CRXP1A7WOeoJeXxjNni43kdQwgnWNReilDMblYTn_I0",
}

let apiUrl = fallback.NEXT_PUBLIC_SUPABASE_URL
let anonKey = fallback.NEXT_PUBLIC_SUPABASE_ANON_KEY

try {
  const out = execSync("npx supabase status -o env", {
    cwd: root,
    encoding: "utf8",
    stdio: ["pipe", "pipe", "pipe"],
  })
  for (const line of out.split("\n")) {
    const m = line.match(/^API_URL="(.+)"$/)
    if (m) apiUrl = m[1]
    const k = line.match(/^ANON_KEY="(.+)"$/)
    if (k) anonKey = k[1]
  }
} catch {
  console.warn("supabase not running — using defaults from config.toml ports")
}

const content = `# Auto-generated by pnpm supabase:env-sync
# Local ports: API ${apiUrl}, Studio http://127.0.0.1:54623

NEXT_PUBLIC_SUPABASE_URL=${apiUrl}
NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}
`

writeFileSync(envPath, content)
console.log(`Wrote ${envPath}`)
console.log(`  NEXT_PUBLIC_SUPABASE_URL=${apiUrl}`)
